package com.company.hw1_6;

public class BasicDataTypes {
    public static void main(String[] args) {
        byte bytyValue = 127;
        short shortValue = 32_767;
        int intValue = 214_748_364;
        long longValue = 999_999_999_999L;
        float floatValue = 544_666_465_465.3F;
        double doubleValue = 56_465.795_646;
        char charC = 169;
        char charM = 181;
        char charR = 174;
        boolean booleanMore = shortValue > bytyValue;

        System.out.println(bytyValue + "\n" + shortValue + "\n" + intValue + "\n" + longValue + "\n" + floatValue + "\n" + doubleValue + "\n" + charC + "\n" + charM + "\n" + charR + "\n" + booleanMore);
    }
}
